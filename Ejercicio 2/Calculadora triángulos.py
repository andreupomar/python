#Práctica 2: Hacer un programa para calcular y mostrar el área de un triángulo
print ("Este programa calcula el área de un triángulo rectángulo.\n")
b= float(input ("Introduzca la base del triángulo \n"))
h= float(input ("Introduzca la altura del triángulo \n"))
A = (b*h)//2
print ("\n El área del triángulo con base %f y altura %f es %f" % (b, h, A))
input ("Presione Enter para cerrar el programa")
