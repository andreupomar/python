#Práctica 2: Hacer un programa para convertir Celsius a Farenheit
print ("Este programa convierte una temperatura en grados centígrados a Farenheit.\n")
C= float(input ("Introduzca la temperatura en ºC que desea convertir a Farenheit \n")) 
F = C*(9//5)+32
print ("\n %f ºC son %f ºF" % (C, F))
input ("Presione Enter para cerrar el programa")
